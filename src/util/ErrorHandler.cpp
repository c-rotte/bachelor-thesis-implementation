#include "ErrorHandler.h"
// --------------------------------------------------------------------------
#include <iostream>
#include <thread>
// --------------------------------------------------------------------------
namespace util {
// --------------------------------------------------------------------------
void raise(const std::string& what) {
    std::cerr << "Thread ["
              << std::this_thread::get_id()
              << "] raised an exception: \""
              << what
              << "\""
              << std::endl;
    throw std::runtime_error(what);
}
// --------------------------------------------------------------------------
}// namespace util
